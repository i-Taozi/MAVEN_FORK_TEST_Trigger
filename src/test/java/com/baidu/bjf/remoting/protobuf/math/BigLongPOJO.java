package com.baidu.bjf.remoting.protobuf.math;

import java.math.BigDecimal;
import java.math.BigInteger;

import com.baidu.bjf.remoting.protobuf.FieldType;
import com.baidu.bjf.remoting.protobuf.annotation.Protobuf;

/**
 * The Class BigLongPOJO.
 *
 * @author xiemalin
 */
public class BigLongPOJO {

    
    @Protobuf(fieldType = FieldType.BIGDECIMAL)
    public BigDecimal value;
    
    
    @Protobuf(fieldType = FieldType.BIGINTEGER)
    public BigInteger value2;

    /* (non-Javadoc)
     * @see java.lang.Object#hashCode()
     */
    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((value == null) ? 0 : value.hashCode());
        result = prime * result + ((value2 == null) ? 0 : value2.hashCode());
        return result;
    }

    /* (non-Javadoc)
     * @see java.lang.Object#equals(java.lang.Object)
     */
    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        BigLongPOJO other = (BigLongPOJO) obj;
        if (value == null) {
            if (other.value != null)
                return false;
        } else if (!value.equals(other.value))
            return false;
        if (value2 == null) {
            if (other.value2 != null)
                return false;
        } else if (!value2.equals(other.value2))
            return false;
        return true;
    } 
    
    
    
}
